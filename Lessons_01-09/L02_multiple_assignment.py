# multiple assignment - allows us to assign multiple variables at the same time in one line of code

# Instead of declaring variables as shown below
# name = 'Nino'
# age = 26
# human = True

# Multiple variable can be assigned on one line with the use of commas
name, age, human = 'Nino', 21, True

print(name)
print(age)
print(human)

# If multiple variables have the same value
apple_price = banana_price = orange_price = 3.00

print(apple_price)
print(banana_price)
print(orange_price)